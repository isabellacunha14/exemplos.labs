#Seu programa irá receber dois números inteiros e irá armazená-los nas variáveis a e b. 
# Como saída, seu programa deve exibir inicialmente os valores das variáveis a e b. 
# Em seguida, seu programa deve efetuar uma sequência de operações aritméticas utilizando as variáveis a e b,
# e exibir o resultado na seguinte ordem:

# Leitura dos dois números inteiros
a = int(input())
b = int(input())

# Exibição dos valores armazenados
print("a:", a)
print("b:", b)

# Operações aritméticas e seus resultados
print("Soma:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Exponenciação:", a ** b)
print("Divisão Inteira:", a // b)
print("Resto da Divisão:", a % b)
