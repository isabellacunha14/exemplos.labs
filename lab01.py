# Leitura dos dois números inteiros
a = int(input())
b = int(input())

# Exibição dos valores armazenados
print("a:", a)
print("b:", b)

# Operações aritméticas e seus resultados
print("Soma:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Exponenciação:", a ** b)
print("Divisão Inteira:", a // b)
print("Resto da Divisão:", a % b)
